/** Pick a random question eligible for the given context. */
export function pickQuestion(context: QuestContext): QuestQuestion {
  const pool = context === 'any'
    ? QUEST_QUESTIONS
    : QUEST_QUESTIONS.filter(q => q.contexts.includes(context) || q.contexts.includes('any'));
  return pool[Math.floor(Math.random() * pool.length)];
}
